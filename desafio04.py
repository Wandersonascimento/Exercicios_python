s = input('Digite algo: ')
print('O tipo primitivo desse valor e:', type(s))
print('Tem espaço? {}'.format(s.isspace))
print('É número?', s.isnumeric())
print('É alphabetico? ', s.isalpha())
print('É Aphanumerico? ', s.isalnum())
print('Está em maiuscula? ', s.isupper())
print('Está em Minuscula? ', s.islower())
print('Esta Capitalizada?', s.istitle())















