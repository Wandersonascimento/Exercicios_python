
#Desafio de n 007, criar um programa que leia duas notas e calcule a média entre elas.

print('Desafio 007')
print('\n{:*^50}\n'.format(' Bem vindo '))
n1 = float(input('Informe a primeira nota: '))
n2 = float(input('Informe a segunda nota: '))
media = (n1 + n2)/2
print('\n A média entre a nota {:.2f} e a nota {:.2f}, é {:.2f} .'.format(n1, n2, media))
print('\n{:*^50}'.format(' Final de programa '))




