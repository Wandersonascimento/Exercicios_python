n1 = int(input('Digite um numero: '))
n2 = int(input('Digite mais um numero: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print(' A soma vale {}; \n A muliplicação vale {}; \n A Divisao vale {:.2f}; \n A divisao por inteiro vale {}; \n A potenciação vale {}.'.format(s, m, d, di, e))
